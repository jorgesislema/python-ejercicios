def frutas_animadas():
    """Frutas con animación"""
    import time
    import os
    
    print("🍓 FRUTAS BAILANDO 🍓")
    
    frutas = ["🍎", "🍌", "🍊", "🍇", "🍓", "🥝", "🍑"]
    
    for i in range(10):
        os.system('cls' if os.name == 'nt' else 'clear')
        print("🍓 FESTIVAL DE FRUTAS 🍓")
        print("═" * 50)
        
        linea1 = ""
        linea2 = ""
        linea3 = ""
        
        for j, fruta in enumerate(frutas):
            if (i + j) % 3 == 0:
                linea1 += f"  {fruta}  "
                linea2 += "     "
                linea3 += "     "
            elif (i + j) % 3 == 1:
                linea1 += "     "
                linea2 += f"  {fruta}  "
                linea3 += "     "
            else:
                linea1 += "     "
                linea2 += "     "
                linea3 += f"  {fruta}  "
        
        print(linea1)
        print(linea2)
        print(linea3)
        
        print("\n🎵 ¡Baila con las frutas! 🎵")
        print("💃 Salud y diversión 🕺")
        time.sleep(0.6)

